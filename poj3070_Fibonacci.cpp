#include <stdio.h>

void solve(int n) {
    int a, b, c, d;
    int f[2][2] = {1, 1, 1, 0}, res[2][2] = {1, 0, 0, 1};
    while (n>0) {
        if (n%2) {
            a = (res[0][0] * f[0][0] + res[0][1] * f[1][0]) % 10000;
            b = (res[0][0] * f[0][1] + res[0][1] * f[1][1]) % 10000;
            c = (res[1][0] * f[0][0] + res[1][1] * f[1][0]) % 10000;
            d = (res[1][0] * f[0][1] + res[1][1] * f[1][1]) % 10000;
            res[0][0] = a;
            res[0][1] = b;
            res[1][0] = c;
            res[1][1] = d;
        }
        a = (f[0][0] * f[0][0] + f[0][1] * f[1][0]) % 10000;
        b = (f[0][0] * f[0][1] + f[0][1] * f[1][1]) % 10000;
        c = (f[1][0] * f[0][0] + f[1][1] * f[1][0]) % 10000;
        d = (f[1][0] * f[0][1] + f[1][1] * f[1][1]) % 10000;
        f[0][0] = a;
        f[0][1] = b;
        f[1][0] = c;
        f[1][1] = d;
        n /= 2;
    }
    printf("%d\n", res[0][1]);
} 

int main() {
    int n;
    while (scanf("%d", &n)) {
        if (n==-1) break;
        solve(n);
    }
}